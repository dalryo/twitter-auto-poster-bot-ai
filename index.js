const GenAI = require("@google/generative-ai");
const { TwitterApi } = require("twitter-api-v2");
const SECRETS = require("./SECRETS");

const twitterClient = new TwitterApi({
  appKey: SECRETS.APP_KEY,
  appSecret: SECRETS.APP_SECRET,
  accessToken: SECRETS.ACCESS_TOKEN,
  accessSecret: SECRETS.ACCESS_SECRET,
});

const generationConfig = {
  maxOutputTokens: 400,
};
const genAI = new GenAI.GoogleGenerativeAI(SECRETS.GEMINI_API_KEY);

async function run() {
  // For text-only input, use the gemini-pro model
  const model = genAI.getGenerativeModel({
    model: "gemini-pro",
    generationConfig,
  });

  // Write your prompt here
  const prompt =
    "generate incredibly controversial and insightful discourse, use gen z slang like bruh, bro, or girl using acronyms like LMAOO, lol, wtf on many posts but not every single post; post only one statement at a time, use all lowercase, use minimal commas and refrain from using capital letters or proper grammar at all; it should be designed to rage bait, related to sports, politics, donald trump, pop culture, superstar celebrities, anything you see fit; if sports, list the specific team name; your goal is to make viral tweets, piss of swifties and republicans and democrats alike; this bot hates taylor swift and loves controversial political posts; it should not be vague and should be unique; under 280 characters and should be plain text, you can use emojis";

  const result = await model.generateContent(prompt);
  const response = await result.response;
  const text = response.text();
  console.log(text);
  sendTweet(text);
}

run();

async function sendTweet(tweetText) {
  try {
    await twitterClient.v2.tweet(tweetText);
    console.log("Tweet sent successfully!");
  } catch (error) {
    console.error("Error sending tweet:", error);
  }
}
